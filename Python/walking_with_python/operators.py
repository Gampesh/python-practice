x = 5
y = 2

# Arithmetic operators
print('x + y =', x + y)
print('x - y =', x - y)
print('x * y =', x * y)
print('x / y =', float(x) / y)
print('x // y =', x // y)
print('x ** y =', x**y)

# Comparison Operators
print('x > y  is', x > y)
print('x < y  is', x < y)
print('x == y is', x == y)
print('x != y is', x != y)
print('x >= y is', x >= y)
print('x <= y is', x <= y)

# Logical Operators
p = True
q = False
print('p and q is', p and q)
print('p or q is', p or q)
print('not p is', not p)


# Bitwise operators with precedence
print('Bitwise x and y', x & y)
print('Bitwise x or y', x | y)
print('Bitwise NOT x', ~x)
print('Bitwise XOR x, y', x ^ y)
print('Bitwise right shift x by 2', x >> 2)
print('Bitwise left shift x by 2', x << 2)


